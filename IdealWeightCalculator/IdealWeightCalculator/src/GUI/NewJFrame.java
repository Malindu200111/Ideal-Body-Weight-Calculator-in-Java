    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String UName = Name.getText();
        double UWeight = Double.parseDouble(Weight.getText());
        double UHeight = Double.parseDouble(Height.getText());
        String UGender = Gender.getText();
        
        double heightInches = UHeight / 2.54;
        double idealWeight = 0;
        

        
        
        // Apply Devine Formula based on gender
        if (UGender.equals("Male")) {
            idealWeight = 50 + 2.3 * (heightInches - 60);
        } else if (UGender.equals("Female")) {
            idealWeight = 45.5 + 2.3 * (heightInches - 60);
        } else {
            System.out.println("Invalid gender input! Please enter Male or Female.");
            return; // Exit if input is invalid
        }
        
        // Calculate ideal weight range (Â±10%)
        double minWeight = idealWeight * 0.9;
        double maxWeight = idealWeight * 1.1;
        
        
        // Display results
        System.out.printf("Your Ideal Body Weight (IBW) is: %.2f kg\n", idealWeight);
        System.out.printf("Your Ideal Weight Range: %.2f kg - %.2f kg\n", minWeight, maxWeight);
        System.out.printf("Your Actual Weight is %.2f kg\n", UWeight);
        
        // Provide health tips based on weight status
        
        String otext = " ";
        if (UWeight < minWeight) {
             otext = " You are UNDERWEIGHT. Consider increasing your calorie intake with protein-rich foods.";
            System.out.println(" You are UNDERWEIGHT. Consider increasing your calorie intake with protein-rich foods.");
        } else if (UWeight > maxWeight) {
             otext = " You are OVERWEIGHT. A balanced diet and regular exercise can help you maintain a healthy weight.";
            System.out.println(" You are OVERWEIGHT. A balanced diet and regular exercise can help you maintain a healthy weight.");
        } else {
             otext = " You are within the ideal weight range. Keep maintaining a healthy lifestyle!";
            System.out.println(" You are within the ideal weight range. Keep maintaining a healthy lifestyle!");
        }

        // Provide health tips based on weight status
        
        
                try (Connection conn = TestMySQLConnection.getConnection()) {
            String query = "INSERT INTO users_bmi (name, gender, weight, height,idealWeight,text) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, UName);
            
            stmt.setString(2, UGender);
            stmt.setDouble(3, UWeight);
            stmt.setDouble(4, UHeight);
            stmt.setDouble(5, idealWeight);
            stmt.setString(6, otext);

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "User data saved successfully!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving user data.");
        }
                
        OutPut outPutFrame = new OutPut(UName,idealWeight,minWeight,UWeight);
        outPutFrame.setVisible(true);
        
    }//GEN-LAST:event_SubmitActionPerformed

